package org.cocos2dx.javascript.entity;

public class NormalIntBean {

    /**
     * code : 0
     * msg : null
     * url : null
     * data : 40
     */

    private int code;
    private String msg;
    private Object url;
    private String data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Object getUrl() {
        return url;
    }

    public void setUrl(Object url) {
        this.url = url;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
